// Print numbers from N down to 1 using a while loop
public class A_2 {
    public static void main(String[] args){
        // Choose a number to start (10)
        int i = 10;
        // Until its equal to 1, print i and decrease it by 1 in each iteration
        while (i >= 1) {
            System.out.println(i);
            i--;
        }
    }
}
